package me.develop_han.modern_java_in_action.chapter1.finalversion;

public class Apple {
	private Color color;
	private int weight;

	public Apple(Color color, int weight) {
		this.color = color;
		this.weight = weight;
	}

	public Color getColor() {
		return color;
	}

	public int getWeight() {
		return weight;
	}
}
